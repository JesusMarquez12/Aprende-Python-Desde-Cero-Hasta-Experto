# Rules and conventions in variable names

# Strict Rules Examples
user_name = 'Juan Perez'
# 1user_name = 'Karla Gomez'    # Cannot start with a number

# Non reserved words use
# class = 'Mi clase'            # Class is a reserved word
klass = 'Mi clase'

# Case Sensitive
name = 'Juan'
Name = 'Karla'
print(name)
print(Name)
# print(NOMBRE)                 # This variable is not defined

# Snake case
full_name = 'Ricardo Esparza'

# Prefix and suffix
is_married = True
name_txt = 'file.txt'